/* Assignment Operator */
let x = 0;
// Assignment Dasar =
let nama = "Ranggo";
console.log(nama);

// // Assignment dan Penjumlahan
let sum = 7;
x = x + 3; //7 + 3 = Output : 10
sum += 3; //7 + 3 = Output : 10

console.log(sum);

// Assignment dan Pengurangan
let dif = 7;
x = x - 3; //7 - 3 = Output : 4
dif -= 3; //7 - 3 = Output : 4

console.log(dif);

// Assignment dan Perkalian
let mul = 7;
x = x * 3; //7 * 3 = Output : 21
mul *= 3; //7 * 3 = Output : 21

console.log(mul);

// Assignment dan Pembagian
let bagi = 12;
x = x / 3; //12 / 3 = Output : 4
bagi /= 3; // 12 / 3 = Output : 4

console.log(bagi);

// Assignment dan Modulus
let mod = 13;
x = x % 3; //13 % 3 = Output : 1
mod %= 3; // 13 % 3 = Output : 1

console.log(mod);
