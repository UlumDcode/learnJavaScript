/* Operator Aritmatika */

// Penjumlahan + (Sumary)

let x = 5;
let y = 10;
let sum = x + y; //output : 15

// console.log(sum, typeof sum)

// Pengurangan - ()

let a = 7;
let b = 3;
let diff = a - b; //output : 4

// console.log(diff, typeof diff)

// Operasi Perkalian * (Multiply)
let p = 5;
let q = 6;
let result = p * q; //output : 30

// console.log(result)

// Operasi pembagian / ()
let m = 100;
let n = 4;
let bagi = m / n; // Output: 25

// console.log(bagi)

// Operasi Modulus % (Hasil bagi)
let r = 17;
let s = 4; //16
let mod = r % s; //Output : 3

// console.log(mod)

// Operasi Eksponen ** (Pangkat)
let base = 2;
let exponent = 3;
let power = base ** exponent; // Output : 8

// console.log(power)

//Operator Increment (Penambahan 1 1)
let count = 5;
count++;

// console.log(count)

//Operator Decrement (pengurangan 1 1)
let countDown = 5;
countDown--;

